//
const char* qimstr(uint8_t button)
{
	static const char* ButtonNames[] =
	{
		"LEFT",
		"RIGHT",
		"MIDDLE",
		"PREV",
		"NEXT"
	};

	return button < QN_COUNTOF(ButtonNames) ? ButtonNames[button] : "UNKNOWN";
}

